package Java_Lista_11_ForEach_com_Vetor;

public class _atividade_2 {
    public static void main(String[] args) {
        String[] c1 = {"São Paulo", "Rio de Janeiro", "Curitiba", "Chicago", "Salvador"};

        System.out.println("Cidades listadas pelo programa:");

        for (String c2 : c1) {
            System.out.println(c2);
        }
    }
}
